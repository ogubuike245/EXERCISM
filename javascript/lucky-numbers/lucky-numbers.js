/**
 * Calculates the sum of the two input arrays.
 *
 * @param {number[]} array1
 * @param {number[]} array2
 * @returns {number} sum of the two arrays
 */
export function twoSum(array1, array2) {
  const one = String(array1.join(""));
  const two = String(array2.join(""));

  return Number(one) + Number(two);
}

